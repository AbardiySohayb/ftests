        public async Task<IActionResult> OnPostAsync(string returnUrl = null)
        {
            returnUrl ??= Url.Content("~/");

            ExternalLogins = (await _signInManager.GetExternalAuthenticationSchemesAsync()).ToList();

            if (ModelState.IsValid)
            {
                var result = await _signInManager.PasswordSignInAsync(Input.Email, Input.Password, Input.RememberMe, lockoutOnFailure: false);
                if (result.Succeeded)
                {
                    // Récupérer l'utilisateur
                    var user = await _userManager.FindByEmailAsync(Input.Email);

                    // Récupérer les rôles de l'utilisateur
                    var roles = await _userManager.GetRolesAsync(user);

                    _logger.LogInformation("User logged in.");

                    // Utiliser les rôles comme nécessaire
                    if (roles.Contains("Etd"))
                    {
                        // L'utilisateur a le rôle "etd"
                        // Faites quelque chose en conséquence, par exemple, rediriger vers une page spécifique pour les étudiants
                        return RedirectToAction("ListeEtd", "Etd");

                    }
                    else if (roles.Contains("Prof"))
                    {
                        // L'utilisateur a le rôle "prof"
                        // Faites quelque chose en conséquence, par exemple, rediriger vers une page spécifique pour les professeurs
                        return RedirectToPage("/Prof/Index");
                    }
                    else if (roles.Contains("Admin"))
                    {
                        // L'utilisateur a le rôle "admin"
                        // Faites quelque chose en conséquence, par exemple, rediriger vers une page spécifique pour les administrateurs
                        return RedirectToAction("DemList", "Dem");
                    }

                    return LocalRedirect(returnUrl);
                }

                // Le reste du code reste inchangé
                if (result.RequiresTwoFactor)
                {
                    return RedirectToPage("./LoginWith2fa", new { ReturnUrl = returnUrl, RememberMe = Input.RememberMe });
                }
                if (result.IsLockedOut)
                {
                    _logger.LogWarning("User account locked out.");
                    return RedirectToPage("./Lockout");
                }
                else
                {
                    ModelState.AddModelError(string.Empty, "Invalid login attempt.");
                    return Page();
                }
            }

            // If we got this far, something failed, redisplay form
            return Page();
        }


